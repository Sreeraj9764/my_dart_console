bool isContains(List<Map<String, dynamic>> source, Map target) {
  for (Map<String, dynamic> e in source) {
    bool matchingMap = true;
    for (String key in target.keys) {
      dynamic v = getMapValueRecursively(key.split("."), e);

      matchingMap = matchingMap && v == target[key];
    }
    if (matchingMap) return true;
  }
  return false;
}

dynamic getMapValueRecursively(
    List<String> keys, Map<String, dynamic>? curStateValue) {
  if (keys.length == 1 || curStateValue == null) {
    return (curStateValue ?? {})[keys[0]];
  } else {
    return getMapValueRecursively(
        keys.sublist(1), HMapEncoder().encode(curStateValue[keys[0]]) ?? {});
  }
}

class HMapEncoder extends PlatformValueEncoder {
  Map<String, dynamic>? encode(dynamic value) {
    if (value is String) {
      return json.decode(value);
    } else {
      return value != null && value is Map
          ? Map<String, dynamic>.from(value)
          : null;
    }
  }
}

abstract class PlatformValueEncoder {
  dynamic encode(dynamic value);
}
